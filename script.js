document.addEventListener('DOMContentLoaded', () => {
    const colorInput = document.getElementById('color-input');
    const bgColorInput = document.getElementById('bg-color-input');
    const dotStyleSelect = document.getElementById('dot-style');
    const cornerStyleSelect = document.getElementById('corner-style');
    const logoUpload = document.getElementById('logo-upload');
    const logoFilename = document.getElementById('logo-filename');
    const removeLogoBtn = document.getElementById('remove-logo-btn');
    const downloadBtn = document.getElementById('download-btn');
    const qrCodeDiv = document.getElementById('qrcode');
    const uploadTriggerBtn = document.getElementById('upload-trigger-btn');
    const languageSelect = document.getElementById('language-select');
    const dataTypeSelect = document.getElementById('data-type');
    const dynamicInputsContainer = document.getElementById('dynamic-inputs');

    let currentLogoUrl = null;
    let selectedFilename = '';

    // Language handling
    function updateLanguage(lang) {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerText = translations[lang][key];
                if (key === 'no_file' && selectedFilename) {
                    el.innerText = selectedFilename;
                }
            }
        });
        localStorage.setItem('qr-lang', lang);
    }

    const savedLang = localStorage.getItem('qr-lang') || 'ko';
    languageSelect.value = savedLang;
    updateLanguage(savedLang);

    languageSelect.addEventListener('change', (e) => {
        updateLanguage(e.target.value);
    });

    // Formatting Data based on selected type
    function formatData() {
        const type = dataTypeSelect.value;
        switch(type) {
            case 'url':
                return document.getElementById('url-input').value.trim() || 'https://example.com';
            case 'wifi':
                const ssid = document.getElementById('wifi-ssid').value.trim();
                const pw = document.getElementById('wifi-password').value;
                const enc = document.getElementById('wifi-encryption').value;
                if(!ssid) return 'WIFI:S:;T:nopass;P:;;';
                return `WIFI:S:${ssid};T:${enc};P:${pw};;`;
            case 'vcard':
                const name = document.getElementById('vcard-name').value.trim();
                const phone = document.getElementById('vcard-phone').value.trim();
                const email = document.getElementById('vcard-email').value.trim();
                const site = document.getElementById('vcard-website').value.trim();
                return `BEGIN:VCARD\nVERSION:3.0\nN:${name}\nTEL:${phone}\nEMAIL:${email}\nURL:${site}\nEND:VCARD`;
            case 'event':
                const title = document.getElementById('event-title').value.trim();
                const startStr = document.getElementById('event-start').value;
                const endStr = document.getElementById('event-end').value;
                const loc = document.getElementById('event-location').value.trim();
                
                const formatTime = (t) => {
                    if(!t) return '';
                    const d = new Date(t);
                    return d.toISOString().replace(/[-:]/g, '').split('.')[0] + 'Z';
                };
                
                return `BEGIN:VEVENT\nSUMMARY:${title}\nDTSTART:${formatTime(startStr) || ''}\nDTEND:${formatTime(endStr) || ''}\nLOCATION:${loc}\nEND:VEVENT`;
            default:
                return 'https://example.com';
        }
    }

    // Initialize QR Code Styling instance
    const qrCode = new QRCodeStyling({
        width: 320,
        height: 320,
        type: "svg",
        data: formatData(),
        image: "",
        margin: 10,
        qrOptions: {
            typeNumber: 0,
            mode: "Byte",
            errorCorrectionLevel: "H"
        },
        imageOptions: {
            hideBackgroundDots: true,
            imageSize: 0.4,
            margin: 5,
            crossOrigin: "anonymous",
        },
        dotsOptions: {
            color: colorInput.value,
            type: dotStyleSelect.value
        },
        backgroundOptions: {
            color: bgColorInput.value,
        },
        cornersSquareOptions: {
            color: colorInput.value,
            type: cornerStyleSelect.value
        },
        cornersDotOptions: {
            color: colorInput.value,
            type: cornerStyleSelect.value === 'extra-rounded' ? 'dot' : (cornerStyleSelect.value === 'square' ? 'square' : 'dot')
        }
    });

    // Mount to DOM
    qrCode.append(qrCodeDiv);

    // Update Function
    function updateQRCode() {
        const data = formatData();
        const dotColor = colorInput.value;
        const bgColor = bgColorInput.value;
        const dotStyle = dotStyleSelect.value;
        const cornerStyle = cornerStyleSelect.value;

        let cornerDotType = 'dot';
        if(cornerStyle === 'square') cornerDotType = 'square';
        if(cornerStyle === 'extra-rounded') cornerDotType = 'dot';

        qrCode.update({
            data: data,
            image: currentLogoUrl || "",
            dotsOptions: {
                color: dotColor,
                type: dotStyle
            },
            backgroundOptions: {
                color: bgColor
            },
            cornersSquareOptions: {
                color: dotColor,
                type: cornerStyle
            },
            cornersDotOptions: {
                color: dotColor,
                type: cornerDotType
            }
        });
    }

    // Event Listeners for Dynamic Types
    dataTypeSelect.addEventListener('change', (e) => {
        document.querySelectorAll('.input-section').forEach(sec => sec.classList.remove('active'));
        document.querySelector(`.input-section[data-type="${e.target.value}"]`).classList.add('active');
        updateQRCode();
    });

    // Event Listeners for real-time updates
    dynamicInputsContainer.addEventListener('input', updateQRCode);
    dynamicInputsContainer.addEventListener('change', updateQRCode);
    colorInput.addEventListener('input', updateQRCode);
    bgColorInput.addEventListener('input', updateQRCode);
    dotStyleSelect.addEventListener('change', updateQRCode);
    cornerStyleSelect.addEventListener('change', updateQRCode);

    // Logo Upload Handling
    uploadTriggerBtn.addEventListener('click', () => {
        logoUpload.click();
    });

    logoUpload.addEventListener('change', (e) => {
        const file = e.target.files[0];
        if (file) {
            selectedFilename = file.name;
            logoFilename.textContent = selectedFilename;
            removeLogoBtn.style.display = 'block';
            
            const reader = new FileReader();
            reader.onload = (event) => {
                currentLogoUrl = event.target.result;
                updateQRCode();
            };
            reader.readAsDataURL(file);
        }
    });

    removeLogoBtn.addEventListener('click', () => {
        logoUpload.value = '';
        currentLogoUrl = null;
        selectedFilename = '';
        logoFilename.textContent = translations[languageSelect.value]['no_file'];
        removeLogoBtn.style.display = 'none';
        updateQRCode();
    });

    // Download Function
    downloadBtn.addEventListener('click', () => {
        qrCode.download({ name: "premium-qrcode", extension: "png" });
    });
});
